from .roteirista_crew import RoteiristaCrew
from .roteirista_config import default_config
from typing import Any, Dict

def roteirista_run(inputs: Dict[str, str]):
    """
    Run the crew.
    """
    default = default_config()   
    agentes_config = default['agente_config']
   
    RoteiristaCrew(agentes_config).crew().kickoff(inputs=inputs)


